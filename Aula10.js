//Arrays | Manipulação

var lista = ["feijão","batata","arroz","limão","goiaba"];

//identificar posição de um array
lista.indexOf;

//Gerar uma string da lista
lista.join(",");

//Remover o ultimo elemento do array
lista.pop();

//Remover o primeiro elemento do array
lista.shift();

//Adicionar um item na ultima posição
lista.push("macarrão");